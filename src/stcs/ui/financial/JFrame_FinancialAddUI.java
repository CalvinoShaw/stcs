    private void Button_sureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_sureActionPerformed
        FinancialVO vo = new FinancialVO();
        vo.setF_type(f_type.getText());
        vo.setF_money(f_money.getText());
        vo.setF_date(f_date.getText());
        vo.setF_associate(f_associate.getText());
        vo.setF_desc(f_desc.getText());
        vo.setF_recorder(f_recorder.getText());

        FinancialBO bo = new FinancialBO();
        boolean rtn = bo.doAdd(vo);
        
        Main.stMenu.loadFinancialData();
        
        this.setVisible(false);
        Main.financialAdd = null;
        Main.stMenu.loadFinancialData();
    }//GEN-LAST:event_Button_sureActionPerformed

